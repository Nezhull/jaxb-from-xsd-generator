package t.issue.xsd.jaxb.generator;

public class ObjectGenerationException extends Exception {
	private static final long serialVersionUID = 9188703278887272926L;

	public ObjectGenerationException() {
		super();
	}

	public ObjectGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public ObjectGenerationException(String message) {
		super(message);
	}

	public ObjectGenerationException(Throwable cause) {
		super(cause);
	}

}
